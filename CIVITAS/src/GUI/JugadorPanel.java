    public void setJugador(Jugador otro){
        this.jugador = otro;

      
        if (this.jugador.getEncarcelado())
            this.encarcelado.setText("SI");
        else
            this.encarcelado.setText("NO");
        
        if(this.jugador.isEpeculador())
            this.especulador.setText("SI");
        else
            this.especulador.setText("NO");
        
        
        this.saldo.setText( "" + this.jugador.getSaldo());
        
        this.nombre.setText(this.jugador.getNombre());
        
        
        this.repaint();
        
        this.rellenaPropiedades(this.jugador.getPropiedades());
        
    }
    
    private void rellenaPropiedades (ArrayList<TituloPropiedad> lista) {
    // Se elimina la información antigua
    propiedades.removeAll();
    this.listaPropiedades.removeAll();
    // Se recorre la lista de propiedades para ir creando sus vistas individuales y
   //añadirlas al panel
    for (TituloPropiedad t : lista) {
        //PropiedadPanel vistaPropiedad = new PropiedadPanel();
       // vistaPropiedad.setPropiedad(t);

        //propiedades.add(vistaPropiedad);
        //vistaPropiedad.setVisible(true);
        
        this.listaPropiedades.add(t.getNombre());
    }
    // Se fuerza la actualización visual del panel propiedades y del panel del jugador
    repaint();
    revalidate();
   }

