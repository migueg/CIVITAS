    private void listaPropiedadesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listaPropiedadesMouseClicked
        // TODO add your handling code here:
        propiedades.removeAll();
        
        String t = this.listaPropiedades.getSelectedItem();
        TituloPropiedad titulo  = null;
        if(t != null || t != "")
            titulo = this.jugador.getTitulo(t);
        
        if(titulo != null){
            PropiedadPanel vistaPropiedad = new PropiedadPanel();
            vistaPropiedad.setPropiedad(titulo);

            propiedades.add(vistaPropiedad);
            vistaPropiedad.setVisible(true); 
        }
        
        repaint();
        revalidate();

        
    }//GEN-LAST:event_listaPropiedadesMouseClicked


